<dt>Percentages</dt>
<dd><p>The 'All Pupils' row represents figures for the entire cohort. Every other row percentage is derived from this figure.</p></dd>
<dt>Show/Hide colour coding</dt>
<dd><p>The traffic light colour coding is used to highlight whether the percentage is Green - above target, Amber - on target or Red - below target. You can
use the show/hide link to toggle the colour coding.</p></dd>
<dt>Show/Hide target %</dt>
<dd><p>This toggles the display of the target percentage under the DCP percentage.</p></dd>
<dt>Achievers</dt>
<dd><p>Achievers are all the pupils achieving a result for the currently selected data collection point (DCP).</p></dd>
<dt>Non Achievers</dt>
<dd><p>Non achievers are all the pupils not achieving a result for the currently selected data collection point (DCP).</p></dd>
<dt>Should Achievers</dt>
<dd><p>Should achievers are all the pupils not achieving a result for the currently selected data collection point (DCP) who should be according to their target.</p></dd>
