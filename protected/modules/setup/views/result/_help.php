<dl>
<dt>General Information</dt>
<dd>
<p>Result name can be edited directly by clicking on the <span class="editable">underlined text</span>.
This is also true of the description when viewing a result set.</p>
</dd>

<dt>No. Records</dt>
<dd>
<p>The number or records in the imported file.</p>
</dd>

<dt>Result Name</dt>
<dd>
<p>The name of the result set. If a result name is not provided on import then the file name will
be used. If the name clashes with another result set name a unique number will be prepended.</p>
</dd>

<dt>Filename</dt>
<dd>
<p>The name of the imported CSV file.</p>
</dd>

<dt>User</dt>
<dd>
<p>The user who imported the file.</p>
</dd>

<dt>Date/Time</dt>
<dd>
<p>The date and time the file was imported.</p>
</dd>

<dt>Description</dt>
<dd>
<p>Available when viewing a result set, a description can be used to further describe the contents
of the imported file.</p>
</dd>

</dl>
